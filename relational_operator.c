#include <stdio.h>

int main()
{
    // true : 1 false : 0
    int a = 5, b = 10;
    printf("a == b : %d\n", a == b);
    printf("a > b : %d\n", a > b);
    printf("a < b : %d\n", a < b);
    printf("a >= b : %d\n", a >= b);
    printf("a <= b : %d\n", a <= b);
    printf("a != b : %d\n", a != b);

    return 0;
}
